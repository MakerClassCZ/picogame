class _Sheet:
    width = _W
    height = FH

    def __getitem__(self, key):
        x, y = key
        if 0 <= x < _W and 0 <= y < FH:
            return (_DATA[y * _STRIDE + (x >> 3)] >> (7 - (x & 7))) & 1
        return 0


_SHEET = _Sheet()


class _Glyph:
    def __init__(self, ti):
        self.bitmap = _SHEET
        self.tile_index = ti


class _Font:
    def get_bounding_box(self):
        return (FW, FH)

    def get_glyph(self, cp):
        if 32 <= cp <= 126:
            return _Glyph(cp - 32)
        return None


FONT = _Font()
